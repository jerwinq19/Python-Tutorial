# What is input?
# Input is a way for your program to "talk" with the user.
# You can ask the user questions, like their name or age,
# and then store their answers to use later in your program.

# What is output?
# Output is how your program "responds" or gives information back to the user.
# Usually, we use the print() function for output.

# Example:
# Here, we ask the user for their name (input)
# then print a message that includes their name (output).

name = input("What is your name? ")  # Ask user for input
print("Your name is " + name)        # Print output back to user
